package com.hotbody.hotShop.board;

public class Supply {
	private int supplycode;
	private String supplyname;
	private String supplytel;
	private String supplypd;
	public int getSupplycode() {
		return supplycode;
	}
	public void setSupplycode(int supplycode) {
		this.supplycode = supplycode;
	}
	public String getSupplyname() {
		return supplyname;
	}
	public void setSupplyname(String supplyname) {
		this.supplyname = supplyname;
	}
	public String getSupplytel() {
		return supplytel;
	}
	public void setSupplytel(String supplytel) {
		this.supplytel = supplytel;
	}
	public String getSupplypd() {
		return supplypd;
	}
	public void setSupplypd(String supplypd) {
		this.supplypd = supplypd;
	}
	
	
}
