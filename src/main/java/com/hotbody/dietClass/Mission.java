package com.hotbody.dietClass;

public class Mission {
	private int classNum, missDay;
	
	private int missionNum, missIndex;
	private String missionContent;
	
	public int getClassNum() {
		return classNum;
	}
	public void setClassNum(int classNum) {
		this.classNum = classNum;
	}
	public int getMissDay() {
		return missDay;
	}
	public void setMissDay(int missDay) {
		this.missDay = missDay;
	}
	public int getMissionNum() {
		return missionNum;
	}
	public void setMissionNum(int missionNum) {
		this.missionNum = missionNum;
	}
	public String getMissionContent() {
		return missionContent;
	}
	public void setMissionContent(String missionContent) {
		this.missionContent = missionContent;
	}
	public int getMissIndex() {
		return missIndex;
	}
	public void setMissIndex(int missIndex) {
		this.missIndex = missIndex;
	}
}
